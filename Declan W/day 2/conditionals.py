temp = int(input("What is the temperature outside?"))

if temp > 97:
    print("You're going to be hot")
elif temp == -50:
    print("You are a popsicle.")
elif temp == 500:
    print("AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA")
elif temp < -100:
    print("Bro is a polar bear")
else:
    print("It's " + str(temp) + " outside, what a great day for a picnic")
